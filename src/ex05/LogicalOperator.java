package ex05;

public class LogicalOperator {
    public static void main(String[] args) {
        int x = 3;
        int y = 4;

        // 논리연산자
        // && 그리고 (and)
        // || 거나 (or)
        // 논리연산은 boolean 타입이다.
        System.out.println((x == 3) && (y == 7));
        System.out.println((x == 3) || (y == 4));
    }

}
