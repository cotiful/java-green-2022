package ex06;

public class ConEx02 {
    public static void main(String[] args) {
        int point = 81;

        if (point >= 90) {
            System.out.println("A");
        } else if (point >= 80) {
            System.out.println("b");
        } else if (point >= 70) {
            System.out.println("c");
        } else if (point >= 60) {
            System.out.println("d");
        } else {
            System.out.println("f");
        }
    } /// 메인 끝
}
